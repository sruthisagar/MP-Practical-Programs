ASSUME CS: CODE, DS: DATA
DATA SEGMENT
	ARR DW 0ABCDH, 1234H, 4567H, 7594H
	LEN DW $-ARR
	LARGE DW 1 DUP(?)
	SMALL DW 1 DUP(?)
DATA ENDS

CODE SEGMENT
START:	MOV AX, DATA
		MOV DS, AX

		LEA SI, ARR
		MOV CX, LEN
		SHR CX, 1
		MOV AX, 0000H
		MOV BX, ARR[SI]

LABEL1:	CMP AX, ARR[SI]
		JNC LABEL2
		MOV AX, ARR[SI]

LABEL2:	CMP BX, ARR[SI]
		JC LABEL3
		MOV BX, ARR[SI]

LABEL3:	INC SI
		INC SI
		LOOP LABEL1
		MOV LARGE, AX
		MOV SMALL, BX

		MOV AH, 4CH
        INT 21H
CODE ENDS
END START