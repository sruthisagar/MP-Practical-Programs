ASSUME CS: CODE, DS: DATA
DATA SEGMENT
	ARR DB 99H, 12H, 56H, 45H, 36H, 11H, 0FEH
	LEN DW $-ARR
	EVENSUM DW 1 DUP(?)	
DATA ENDS

CODE SEGMENT
START:	MOV AX, DATA
		MOV DS, AX

		LEA SI, ARR
		MOV CX, LEN
		MOV BX, 0000H

LABEL1:	MOV AL, ARR[SI]
		AND AL, 01H
		CMP AL, 00H
		JNZ LABEL2
		MOV AH, 00H
		MOV AL, ARR[SI]
		ADD BX, AX

LABEL2:	INC SI
		LOOP LABEL1
		MOV EVENSUM, BX

		MOV AH, 4CH
        INT 21H
CODE ENDS
END START