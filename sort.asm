ASSUME CS: CODE, DS: DATA
DATA SEGMENT
	ARR DB 0ABH, 12H, 45H, 75H, 0C2H
	LEN DW $-ARR
DATA ENDS

CODE SEGMENT
START:	MOV AX, DATA
		MOV DS, AX

		LEA SI, ARR
		MOV AX, LEN

LABEL1:	MOV DI, SI
		INC DI
		MOV CX, AX
		DEC CX

LABEL2:	MOV BH, ARR[SI]
		MOV BL, ARR[DI]
		CMP BH, BL
		JC LABEL3
		MOV ARR[SI], BL
		MOV ARR[DI], BH

LABEL3:	INC DI
		LOOP LABEL2
		INC SI
		DEC AX
		CMP AX, 0001H
		JNZ LABEL1

		MOV AH, 4CH
        INT 21H
CODE ENDS
END START